public class switch_Class {
    public static void main(String[] args) {
        char myletter='e';

        switch (Character.toLowerCase(myletter)){
            case 'a':
                System.out.println("It is a vowel");
                break;
            case 'e':
                System.out.println("It is a vowel");
                break;
                case 'i':
                System.out.println("It is a vowel");
                break;
                case 'o':
                System.out.println("It is a vowel");
                break;
            case 'u':
                System.out.println("It is a vowel");
                break;
            default:
                System.out.println("It is not a vowel" );

        }
    }
}
